#include "libft.h"

int			ft_atoi(const char *s)
{
	long		res;
	long		minus;

	res = 0;
	minus = 1;
	while ((*s >= 9 && *s <= 13) || *s == ' ')
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			minus = -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		if (9223372036854775807 - res < *s - 48)
		{
			if (minus == 1)
				return (-1);
			else
				return (0);
		}
		res = (res * 10) + (*s++ - 48);
	}
	return (res * minus);
}
