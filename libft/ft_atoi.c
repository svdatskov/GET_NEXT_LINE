#include "libft.h"

int			ft_atoi(const char *str)
{
	long	m;
	long	r;

	m = 0;
	r = 0;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			m++;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		if (9223372036854775807 - r < *str - '0')
		{
			if (m == 0)
				return (-1);
			else
				return (0);
		}
		r = r * 10 + (*str++ - '0');
	}
	return (m ? -r : r);
}
