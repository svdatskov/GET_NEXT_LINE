#include "libft.h"

int		ft_charcount(int i)
{
	int			k;
	long int	j;

	j = i;
	k = 0;
	if (i == 0)
		return (1);
	if (j < 0)
	{
		j *= -1;
		k++;
	}
	while ((j - 1) >= 0)
	{
		k++;
		j /= 10;
	}
	return (k);
}
